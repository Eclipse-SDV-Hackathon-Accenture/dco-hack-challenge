package com.tsystems.dco.track.repository;

import com.tsystems.dco.track.entity.TrackEntity;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.JpaSpecificationExecutor;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.UUID;

@Repository
public interface TrackRepository extends JpaRepository<TrackEntity, UUID>, JpaSpecificationExecutor<TrackEntity> {

  @Query(value = "SELECT * FROM track where LOWER(name) like LOWER(?1)", nativeQuery = true)
  Page<TrackEntity> findByTrackLike(String trackPattern, Pageable pageable);

  @Query("select t from track t where id in :ids")
  List<TrackEntity> findTrackByIds(@Param("ids") List<UUID> trackIds);
}
